package org.sklse.owlseditor.xml;

public class XExtensional_Fragment {

	private String Extension_pattern=null;
	private XInput[] Input=null;
	private XOutput[] Output=null;
	private XWsdl_Grounding Wsdl_Grounding=null;
	public String getExtension_pattern() {
		return Extension_pattern;
	}
	public void setExtension_pattern(String extensionPattern) {
		Extension_pattern = extensionPattern;
	}
	public XInput[] getInput() {
		return Input;
	}
	public void setInput(XInput[] input) {
		Input = input;
	}
	public XOutput[] getOutput() {
		return Output;
	}
	public void setOutput(XOutput[] output) {
		Output = output;
	}
	public XWsdl_Grounding getWsdl_Grounding() {
		return Wsdl_Grounding;
	}
	public void setWsdl_Grounding(XWsdl_Grounding wsdlGrounding) {
		Wsdl_Grounding = wsdlGrounding;
	}
	
	
	
}
